#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ptr;
	size_t	n;
	int		i;

	n = ft_strlen(s1) + 1;
	i = 0;
	ptr = malloc(n);
	if (ptr == NULL)
		return (NULL);
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
